let a=4;
let op='+';
let b=5;

switch(op)
{
    case '+':console.log("Addition",a+b);
    break;
    case '-':console.log("Subtraction",a-b);
    break;
    case '*':console.log("Multiplication",a*b);
    break;
    case '/':console.log("Division",a/b);
    break;
    default:console.log("Enter correct operator");
}